SELECT MAX(Weight) AS Max_Weight, MIN(Weight) AS Min_Weight, AVG(Weight) AS Avg_Weight FROM ANIMAL;

SELECT MAX(Amount) AS Max_Amount, MIN(Amount) AS Min_Amount, AVG(Amount) AS Avg_Amount FROM EATING;

SELECT MAX(Salary) AS Max_Salary, MIN(Salary) AS Min_Salary, AVG(Salary) AS Avg_Salary FROM EMPLOYEE;

SELECT MAX(Price) AS Max_Price, MIN(Price) AS Min_Price, AVG(Price) AS Avg_Price FROM PURCHASE;

